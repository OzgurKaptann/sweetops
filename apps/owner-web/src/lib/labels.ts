/**
 * Look a wire value up in one of the maps above.
 *
 * An unknown value renders as `fallback`, never as the raw enum. A manager who
 * sees "Bilinmiyor" knows the row is odd and can ask; one who sees
 * "TRANSFER_OUT_PENDING" has been handed an internal identifier to interpret.
 */
export function labelFor(
  map: Record<string, string>,
  value: string | null | undefined,
  fallback = "Bilinmiyor",
): string {
  if (!value) return fallback;
  return map[value] ?? fallback;
}

