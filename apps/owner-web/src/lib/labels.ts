/**
 * Turkish display labels for the enum values the API sends.
 *
 * The API speaks English enums — TRANSFER_OUT, stock_risk, IN_PREP — and that
 * stays the wire contract: never translated, never renamed, and every comparison
 * in this app is still made against the English value. This module is the one
 * place where those values become manager-readable, so that a raw
 * `PURCHASE_RECEIPT` can never reach the screen.
 *
 * Look values up through the helpers, not by indexing the records directly —
 * the helpers decide what an unknown value renders as.
 */

/**
 * Inventory movement types (`movement_type` on a stock movement).
 *
 * Two pairs here are easy to confuse and expensive to get wrong, so they are
 * named for what physically happened, not for the arithmetic sign:
 *   WASTE            — stock binned. Money lost.
 *   TRANSFER_OUT     — stock shipped to another branch. Money not lost.
 *   PURCHASE_RECEIPT — stock bought from a supplier.
 *   TRANSFER_IN      — stock received from another branch. Nothing was bought.
 * Labelling a transfer as "Fire" or "Mal kabul" would quietly corrupt the
 * owner's read of both waste and purchasing.
 *
 * A third distinction, and the same kind of mistake:
 *   MANUAL_ADJUSTMENT      — "Manuel düzeltme". Someone knew a figure was wrong and
 *                            corrected it.
 *   STOCK_COUNT_ADJUSTMENT — "Sayım düzeltmesi". Someone COUNTED the shelf and the
 *                            system was made to agree with what they found.
 * They are different events with different evidence behind them. Showing a counted
 * discrepancy as "Manuel düzeltme" would hide shrinkage inside a label that means
 * "we decided to change this number".
 */
