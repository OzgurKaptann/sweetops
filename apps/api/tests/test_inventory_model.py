def _insert_movement(db, ing_id: int, **overrides) -> None:
    """
    Insert a raw movement row, bypassing the service layer.

    ``store`` defaults to the suite's store, so the constraint tests below read
    unchanged. Passing a different store is how the cross-store tests prove the
    composite foreign keys reject a ledger row that tries to straddle branches —
    and they do it in raw SQL precisely because the point is that the DATABASE
    refuses, not that the service politely declines.
    """
    params = {
        "store_id": DEFAULT_STORE_ID,
        "ingredient_id": ing_id,
        "movement_type": "PURCHASE_RECEIPT",
        "quantity": Decimal("5.000"),
        "delta_on_hand": Decimal("5.000"),
        "delta_reserved": Decimal("0"),
        "unit": "g",
        "reason": "test",
        "actor": None,
        "legacy": False,
    }
    params.update(overrides)
    db.execute(
        text(
            """
            INSERT INTO ingredient_stock_movements (
                store_id, ingredient_id, movement_type, quantity,
                quantity_delta_on_hand, quantity_delta_reserved,
                unit, reason, actor_user_id, legacy_backfill
            ) VALUES (
                :store_id, :ingredient_id, :movement_type, :quantity,
                :delta_on_hand, :delta_reserved,
                :unit, :reason, :actor, :legacy
            )
            """
        ),
        params,
    )
    db.commit()


class TestMovementConstraints:

    def test_quantity_must_be_positive(self, db):
        ing, _ = make_ingredient(db, on_hand=Decimal("10.000"))
        try:
            for bad in (Decimal("0"), Decimal("-5.000")):
                with pytest.raises(_DB_REJECTS):
                    _insert_movement(
                        db, ing.id, quantity=bad,
                        delta_on_hand=bad, actor=None, legacy=True,
                    )
                db.rollback()
        finally:
            cleanup_ingredient(db, ing.id)

    def test_unknown_movement_type_rejected(self, db):
        ing, _ = make_ingredient(db, on_hand=Decimal("10.000"))
        try:
            with pytest.raises(_DB_REJECTS):
                _insert_movement(db, ing.id, movement_type="TELEPORTED", legacy=True)
            db.rollback()
        finally:
            cleanup_ingredient(db, ing.id)

    def test_deltas_must_match_movement_type(self, db):
        """
        A row cannot claim CONSUMPTION while ADDING to physical stock. This is
        what makes an unqualified signed delta impossible.
        """
        ing, _ = make_ingredient(db, on_hand=Decimal("10.000"))
        try:
            with pytest.raises(_DB_REJECTS):
                _insert_movement(
                    db, ing.id,
                    movement_type="CONSUMPTION",
                    quantity=Decimal("5.000"),
                    delta_on_hand=Decimal("5.000"),   # wrong sign
                    delta_reserved=Decimal("-5.000"),
                    actor=None,
                )
            db.rollback()

            # A reservation must not move physical stock.
            with pytest.raises(_DB_REJECTS):
                _insert_movement(
                    db, ing.id,
                    movement_type="RESERVATION_CREATED",
                    quantity=Decimal("5.000"),
                    delta_on_hand=Decimal("-5.000"),  # must be 0
                    delta_reserved=Decimal("5.000"),
                )
            db.rollback()
        finally:
            cleanup_ingredient(db, ing.id)

    def test_manual_adjustment_requires_actor_and_reason(self, db, make_staff):
        ing, _ = make_ingredient(db, on_hand=Decimal("10.000"))
        owner = make_staff("OWNER")
        try:
            # No actor → refused.
            with pytest.raises(_DB_REJECTS):
                _insert_movement(
                    db, ing.id,
                    movement_type="MANUAL_ADJUSTMENT",
                    quantity=Decimal("2.000"),
                    delta_on_hand=Decimal("2.000"),
                    reason="stok sayimi",
                    actor=None,
                )
            db.rollback()

            # No reason → refused.
            with pytest.raises(_DB_REJECTS):
                _insert_movement(
                    db, ing.id,
                    movement_type="MANUAL_ADJUSTMENT",
                    quantity=Decimal("2.000"),
                    delta_on_hand=Decimal("2.000"),
                    reason="   ",           # whitespace is not a reason
                    actor=owner.id,
                )
            db.rollback()

            # Both present → accepted.
            _insert_movement(
                db, ing.id,
                movement_type="MANUAL_ADJUSTMENT",
                quantity=Decimal("2.000"),
                delta_on_hand=Decimal("2.000"),
                reason="stok sayimi",
                actor=owner.id,
            )
            assert db.query(IngredientStockMovement).filter_by(
                ingredient_id=ing.id, movement_type="MANUAL_ADJUSTMENT"
            ).count() == 1
        finally:
            cleanup_ingredient(db, ing.id)

    def test_waste_requires_reason(self, db, make_staff):
        ing, _ = make_ingredient(db, on_hand=Decimal("10.000"))
        owner = make_staff("OWNER")
        try:
            with pytest.raises(_DB_REJECTS):
                _insert_movement(
                    db, ing.id,
                    movement_type="WASTE",
                    quantity=Decimal("2.000"),
                    delta_on_hand=Decimal("-2.000"),
                    reason=None,
                    actor=owner.id,
                )
            db.rollback()
        finally:
            cleanup_ingredient(db, ing.id)

    def test_ledger_is_append_only(self, db, make_staff):
        """
        UPDATE and DELETE on the ledger are both refused. History is corrected
        with a new compensating movement, never by editing the past.
        """
        ing, _ = make_ingredient(db, on_hand=Decimal("10.000"))
        owner = make_staff("OWNER")
        try:
            _insert_movement(
                db, ing.id,
                movement_type="PURCHASE_RECEIPT",
                quantity=Decimal("5.000"),
                delta_on_hand=Decimal("5.000"),
                reason="teslimat",
                actor=owner.id,
            )
            mv = db.query(IngredientStockMovement).filter_by(ingredient_id=ing.id).first()
            assert mv is not None

            with pytest.raises(_DB_REJECTS):
                db.execute(
                    text("UPDATE ingredient_stock_movements SET quantity = 999 WHERE id = :i"),
                    {"i": mv.id},
                )
                db.commit()
            db.rollback()

            with pytest.raises(_DB_REJECTS):
                db.execute(
                    text("DELETE FROM ingredient_stock_movements WHERE id = :i"),
                    {"i": mv.id},
                )
                db.commit()
            db.rollback()

            # Still exactly as written.
            db.expire_all()
            mv = db.query(IngredientStockMovement).filter_by(ingredient_id=ing.id).first()
            assert mv.quantity == Decimal("5.000")
        finally:
            cleanup_ingredient(db, ing.id)


