"""
Database-level inventory integrity.

These tests bypass the service layer entirely and talk to PostgreSQL directly.
The point is that the guarantees hold even if the application is wrong, bypassed
or malicious: a bad stock state must be *unrepresentable*, not merely
unreachable through the happy path.

Covered:
  1.  quantities are exact Decimal/NUMERIC, never binary float
  2.  reserved_quantity cannot be negative
  3.  on_hand_quantity cannot be negative
  4.  reserved cannot exceed on-hand (no backorders)
  5.  available_quantity is generated by the database (on_hand - reserved)
  6.  movement quantity must be positive
  7.  an unknown movement type is rejected
  8.  deltas must agree with the movement type
  9.  manual adjustment requires an actor and a reason
  10. waste requires a reason
  11. the movement ledger is append-only (UPDATE and DELETE both refused)
  12. an order inventory line cannot settle more than it reserved
"""
import uuid
from decimal import Decimal

import pytest
from sqlalchemy import text
from sqlalchemy.exc import DataError, IntegrityError, ProgrammingError

from app.models.ingredient_stock import (
    IngredientStock,
    IngredientStockMovement,
    OrderInventoryLine,
)
from tests.conftest import (
    DEFAULT_STORE_ID,
    _inventory_maintenance,
    cleanup_ingredient,
    make_ingredient,
)

_DB_REJECTS = (IntegrityError, DataError, ProgrammingError)


# ---------------------------------------------------------------------------
# Stock summary
# ---------------------------------------------------------------------------

class TestStockSummaryConstraints:

    def test_quantities_are_decimal_not_float(self, db):
        """Money and mass are exact. 0.1 g added 3 times must be exactly 0.3 g."""
        ing, stock = make_ingredient(db, on_hand=Decimal("0.300"))
        try:
            db.expire_all()
            row = db.query(IngredientStock).filter_by(ingredient_id=ing.id).first()
            assert isinstance(row.on_hand_quantity, Decimal)
            assert isinstance(row.reserved_quantity, Decimal)
            assert isinstance(row.available_quantity, Decimal)
            # The exact-decimal property that float would break.
            assert (
                Decimal("0.100") + Decimal("0.100") + Decimal("0.100")
                == row.on_hand_quantity
            )
        finally:
            cleanup_ingredient(db, ing.id)

    def test_available_is_generated_by_the_database(self, db):
        """available_quantity is never written by the app — the DB derives it."""
        ing, stock = make_ingredient(
            db, on_hand=Decimal("100.000"), reserved=Decimal("30.000")
        )
        try:
            db.expire_all()
            row = db.query(IngredientStock).filter_by(ingredient_id=ing.id).first()
            assert row.available_quantity == Decimal("70.000")

            # Change an input; the generated column follows automatically.
            row.reserved_quantity = Decimal("40.000")
            db.commit()
            db.expire_all()
            row = db.query(IngredientStock).filter_by(ingredient_id=ing.id).first()
            assert row.available_quantity == Decimal("60.000")
        finally:
            cleanup_ingredient(db, ing.id)

    def test_reserved_cannot_be_negative(self, db):
        ing, stock = make_ingredient(db, on_hand=Decimal("10.000"))
        try:
            with pytest.raises(_DB_REJECTS):
                db.execute(
                    text(
                        "UPDATE ingredient_stock SET reserved_quantity = -1 "
                        "WHERE ingredient_id = :i"
                    ),
                    {"i": ing.id},
                )
                db.commit()
            db.rollback()
        finally:
            cleanup_ingredient(db, ing.id)

    def test_on_hand_cannot_be_negative(self, db):
        ing, stock = make_ingredient(db, on_hand=Decimal("10.000"))
        try:
            with pytest.raises(_DB_REJECTS):
                db.execute(
                    text(
                        "UPDATE ingredient_stock SET on_hand_quantity = -1 "
                        "WHERE ingredient_id = :i"
                    ),
                    {"i": ing.id},
                )
                db.commit()
            db.rollback()
        finally:
            cleanup_ingredient(db, ing.id)

    def test_reserved_cannot_exceed_on_hand(self, db):
        """
        Backorders are disabled: the shop cannot promise batter it does not hold.
        """
        ing, stock = make_ingredient(db, on_hand=Decimal("10.000"))
        try:
            with pytest.raises(_DB_REJECTS):
                db.execute(
                    text(
                        "UPDATE ingredient_stock SET reserved_quantity = 10.001 "
                        "WHERE ingredient_id = :i"
                    ),
                    {"i": ing.id},
                )
                db.commit()
            db.rollback()
        finally:
            cleanup_ingredient(db, ing.id)


