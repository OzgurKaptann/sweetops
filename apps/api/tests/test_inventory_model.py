class TestOrderInventoryLineConstraints:

    def test_settled_cannot_exceed_reserved(self, db, client):
        """
        consumed + released <= reserved. This single constraint is what makes
        double-consumption and consume-then-release structurally impossible,
        even for a caller that bypasses the service.
        """
        from tests.conftest import order_payload

        ing, _ = make_ingredient(
            db, on_hand=Decimal("100.000"), standard_quantity=Decimal("10.00")
        )
        try:
            payload, headers = order_payload(ing.id, idem_key=uuid.uuid4().hex)
            r = client.post("/public/orders/", json=payload, headers=headers)
            assert r.status_code == 200

            line = db.query(OrderInventoryLine).filter_by(ingredient_id=ing.id).first()
            assert line is not None
            assert line.reserved_quantity == Decimal("10.000")

            # Consume more than was ever reserved.
            with pytest.raises(_DB_REJECTS):
                db.execute(
                    text(
                        "UPDATE order_inventory_lines SET consumed_quantity = 10.001 "
                        "WHERE id = :i"
                    ),
                    {"i": line.id},
                )
                db.commit()
            db.rollback()

            # Consume all of it AND release all of it.
            with pytest.raises(_DB_REJECTS):
                db.execute(
                    text(
                        "UPDATE order_inventory_lines "
                        "SET consumed_quantity = 10, released_quantity = 10 "
                        "WHERE id = :i"
                    ),
                    {"i": line.id},
                )
                db.commit()
            db.rollback()
        finally:
            cleanup_ingredient(db, ing.id)

    def test_quantities_cannot_be_negative(self, db, client):
        from tests.conftest import order_payload

        ing, _ = make_ingredient(
            db, on_hand=Decimal("100.000"), standard_quantity=Decimal("10.00")
        )
        try:
            payload, headers = order_payload(ing.id, idem_key=uuid.uuid4().hex)
            client.post("/public/orders/", json=payload, headers=headers)
            line = db.query(OrderInventoryLine).filter_by(ingredient_id=ing.id).first()

            for column in ("consumed_quantity", "released_quantity", "waste_quantity"):
                with pytest.raises(_DB_REJECTS):
                    db.execute(
                        text(
                            f"UPDATE order_inventory_lines SET {column} = -1 WHERE id = :i"
                        ),
                        {"i": line.id},
                    )
                    db.commit()
                db.rollback()
        finally:
            cleanup_ingredient(db, ing.id)
